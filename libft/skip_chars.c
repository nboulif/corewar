char	*skip_chars(char *s, char *chars)
{
	int i;

	i = 0;
	if (!s || !*s)
		return (0);
	while (chars[i])
	{
		if (*s == chars[i])
		{
			s++;
			i = 0;
		}
		else
			i++;
	}
	return (s);
}
