#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	n1;
	size_t	n2;

	n1 = ft_strlen(s1);
	n2 = 0;
	while (s2[n2])
	{
		s1[n1 + n2] = s2[n2];
		n2++;
	}
	s1[n1 + n2] = '\0';
	return (s1);
}
