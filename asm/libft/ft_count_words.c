#include "libft.h"

int		ft_count_words(char const *s, char c)
{
	int i;

	i = *s && *s != c ? 1 : 0;
	while (*s)
	{
		if (*s == c && *(s + 1) != c && *(s + 1))
			i++;
		s++;
	}
	return (i);
}
