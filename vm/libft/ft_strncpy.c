#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, uint32_t len)
{
	uint32_t	n;

	n = 0;
	while (*src && n < len)
		dst[n++] = *src++;
	while (n < len)
		dst[n++] = '\0';
	return (dst);
}
