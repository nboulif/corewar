#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, uint32_t))
{
	if (!alst || !*alst || !del)
		return ;
	if ((*alst)->next)
		ft_lstdel(&(*alst)->next, del);
	ft_lstdelone(&(*alst), del);
}
