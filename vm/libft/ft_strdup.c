#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*s2;
	uint32_t	n;

	if (!(s2 = ft_strnew(ft_strlen(s1))))
		return (0);
	n = 0;
	while (*s1)
		s2[n++] = *s1++;
	s2[n] = '\0';
	return (s2);
}
