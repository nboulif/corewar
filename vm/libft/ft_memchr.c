#include "libft.h"

void	*ft_memchr(const void *s, int c, uint32_t n)
{
	uint32_t	i;

	i = 0;
	while (i < n)
	{
		if ((uint8_t)c == ((uint8_t*)s)[i])
			return ((void *)(s + i));
		i++;
	}
	return (NULL);
}
