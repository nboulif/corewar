#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, uint32_t n)
{
	uint32_t	i;

	i = 0;
	while (i < n)
	{
		((uint8_t *)dst)[i] = ((uint8_t*)src)[i];
		i++;
	}
	return (dst);
}
